package org.hsc.silk.hbc;



public class ProductInfo {
	private String upc;
	private String product;
	private String halalId;
	private String halalBeginDate;
	private String halalExpDate;
	private String otherInfo;
	
	public String getUpc() {
		return upc;
	}
	public void setUpc(String upc) {
		this.upc = upc;
	}
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public String getHalalId() {
		return halalId;
	}
	public void setHalalId(String halalId) {
		this.halalId = halalId;
	}
	public String getHalalBeginDate() {
		return halalBeginDate;
	}
	public void setHalalBeginDate(String halalBeginDate) {
		this.halalBeginDate = halalBeginDate;
	}
	public String getHalalExpDate() {
		return halalExpDate;
	}
	public void setHalalExpDate(String halalExpDate) {
		this.halalExpDate = halalExpDate;
	}
	public String getOtherInfo() {
		return otherInfo;
	}
	public void setOtherInfo(String otherInfo) {
		this.otherInfo = otherInfo;
	}
	
}
